// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Return the configured login slug, or an empty string if none is set.
 *
 * Uses a static variable to avoid repeated get_option() calls within a
 * single request.
 *
 * @return string
 */
function mainframe_get_login_slug(): string {
	static $slug = null;

	if ( null === $slug ) {
		$slug = (string) get_option( 'mainframe_login_slug', '' );
	}

	return $slug;
}

/**
 * Return the current request path relative to the WordPress home path.
 *
 * Strips the home URL path prefix so the comparison in the hooks above
 * works correctly on WordPress installs in subdirectories.
 *
 * @return string Request path starting with '/', e.g. '/login' or '/wp-login.php'.
 */
function mainframe_get_request_path(): string {
	$request_uri = isset( $_SERVER['REQUEST_URI'] ) ? sanitize_text_field( wp_unslash( $_SERVER['REQUEST_URI'] ) ) : '/';
	// Strip query string.
	$path = (string) parse_url( $request_uri, PHP_URL_PATH );

	// Remove the home URL's path prefix for sub-directory installs.
	$home_path = (string) parse_url( home_url(), PHP_URL_PATH );
	if ( $home_path && '/' !== $home_path && str_starts_with( $path, $home_path ) ) {
		$path = substr( $path, strlen( $home_path ) );
	}

	return '/' . ltrim( $path, '/' );
}
