<?php
/**
 * Mainframe Theme — Login URL Customization
 *
 * Implements a custom login URL slug natively in PHP without plugin
 * dependencies. Intercepts requests to the configured slug and routes
 * them to wp-login.php, and blocks direct access to wp-login.php when
 * a custom slug is active.
 *
 * @package Mainframe
 */

defined( 'ABSPATH' ) || exit;

// ---------------------------------------------------------------------------
// Early request interception — must fire before headers are sent.
// ---------------------------------------------------------------------------

add_action( 'init', 'mainframe_handle_login_request', 1 );
/**
 * Intercept requests to the custom login slug and route them to wp-login.php.
 *
 * Runs at priority 1 on 'init' so it fires before any rewrite rules or
 * redirect logic added by the theme or plugins. Does nothing when no slug
 * is configured, leaving WordPress's default behaviour intact.
 *
 * Security considerations:
 * - The slug is sanitized on save (see options.php) so no sanitization is
 *   needed here at read time.
 * - We compare against the raw request URI after stripping the home path so
 *   the check is host-independent.
 * - wp-login.php access is blocked separately below.
 */
function mainframe_handle_login_request(): void {
	$slug = mainframe_get_login_slug();

	if ( empty( $slug ) ) {
		return;
	}

	$request_path = mainframe_get_request_path();

	// Serve wp-login.php for requests matching /<slug> or /<slug>/.
	if ( rtrim( $request_path, '/' ) === '/' . $slug ) {
		// phpcs:ignore WordPressVIPMinimum.Files.IncludingFile.UsingCustomFunction
		require_once ABSPATH . 'wp-login.php';
		exit;
	}
}

add_action( 'init', 'mainframe_block_default_login', 1 );
/**
 * Block direct access to wp-login.php when a custom slug is active.
 *
 * When a custom slug is configured, a request to the bare /wp-login.php
 * path should not succeed — it would defeat the purpose of the rename.
 * We redirect such requests to the home page with a 302 rather than
 * returning a 404, to avoid leaking information about the existence of
 * the login system.
 *
 * Requests that carry a valid action parameter and come from wp-admin
 * redirects (e.g. logout, password reset confirmation) are allowed
 * through so core functionality is not broken. WordPress itself always
 * initiates these via the internal wp_login_url() / admin_url() helpers;
 * we filter those to the custom slug via login_url below.
 *
 * Exception: POST requests to wp-login.php are never blocked — plugins
 * and external systems may rely on POSTing credentials directly.
 */
function mainframe_block_default_login(): void {
	$slug = mainframe_get_login_slug();

	if ( empty( $slug ) ) {
		return;
	}

	$request_path = mainframe_get_request_path();

	if ( rtrim( $request_path, '/' ) !== '/wp-login.php' ) {
		return;
	}

	// Allow POST (credential submission) and requests with a recognised
	// action so core password-reset / logout flows are not broken.
	$method = isset( $_SERVER['REQUEST_METHOD'] ) ? strtoupper( sanitize_text_field( wp_unslash( $_SERVER['REQUEST_METHOD'] ) ) ) : 'GET';
	// phpcs:ignore WordPress.Security.NonceVerification.Recommended
	$action = isset( $_GET['action'] ) ? sanitize_key( $_GET['action'] ) : '';
	$allowed_actions = [ 'logout', 'lostpassword', 'rp', 'resetpass', 'postpass', 'confirmaction', 'register' ];

	if ( 'POST' === $method || in_array( $action, $allowed_actions, true ) ) {
		return;
	}

	wp_safe_redirect( home_url( '/' ), 302 );
	exit;
}

// ---------------------------------------------------------------------------
// Filter WordPress's login URL helpers to use the custom slug.
// ---------------------------------------------------------------------------

add_filter( 'login_url', 'mainframe_filter_login_url', 10, 3 );
/**
 * Replace wp-login.php in URLs generated by wp_login_url() and login_url().
 *
 * WordPress passes the constructed URL through this filter before returning
 * it to callers. We swap the wp-login.php segment for the custom slug so
 * admin redirects, password-reset emails, and any code using the canonical
 * helper all point to the right place.
 *
 * @param string $login_url    The login URL (already has scheme and host).
 * @param string $redirect     The redirect URL passed to wp_login_url().
 * @param bool   $force_reauth Whether to force reauthorization.
 * @return string Modified login URL.
 */
function mainframe_filter_login_url( string $login_url, string $redirect, bool $force_reauth ): string {
	$slug = mainframe_get_login_slug();

	if ( empty( $slug ) ) {
		return $login_url;
	}

	return str_replace( 'wp-login.php', $slug, $login_url );
}

